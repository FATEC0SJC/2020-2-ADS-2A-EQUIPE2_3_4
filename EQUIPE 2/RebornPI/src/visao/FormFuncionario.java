    private void jTableFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFuncionariosMouseClicked
        String nome_funcionario = ""+jTableFuncionarios.getValueAt(jTableFuncionarios.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("select *from funcionarios where nome_funcionario='"+nome_funcionario+"'");
        try {
            conex.rs.first();
            jtCOD.setText(String.valueOf(conex.rs.getInt("cod_funcionario")));
            jTextFieldNome.setText(conex.rs.getString("nome_funcionario"));
            jPasswordFieldSenha.setText(conex.rs.getString("senha_funcionario"));
            jFormattedTextFieldCPF.setText(String.valueOf(conex.rs.getInt("cpf_funcionario")));
            jFormattedTextFieldRG.setText(String.valueOf(conex.rs.getInt("rg_funcionario")));
            jComboBoxCNH.setSelectedItem(conex.rs.getString("cnh_funcionario"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao selecionar Dados:\n"+ex.getMessage());
        }
        conex.desconecta();
        jButtonAlterar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
    }//GEN-LAST:event_jTableFuncionariosMouseClicked

