        public void preencherTabela(String Sql){
    
        ArrayList dados = new ArrayList();
        String[] colunas = new String []{
        "ID","Nome","CPF","Senha","RG","CNH"};
        conex.conexao();
        conex.executaSql(Sql);
        
        
        
        try{
            conex.rs.first();
            do{ 
                dados.add(new Object[]{conex.rs.getInt("cod_funcionario"),conex.rs.getString("nome_funcionario"),conex.rs.getString("senha_funcionario"),conex.rs.getInt("cpf_funcionario"),conex.rs.getInt("rg_funcionario"),conex.rs.getString("cnh_funcionario")});
                
                }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane,"Busque por outro Funcionário");
            
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableFuncionarios.setModel(modelo);
        jTableFuncionarios.getColumnModel().getColumn(0).setPreferredWidth(35);
        jTableFuncionarios.getColumnModel().getColumn(0).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableFuncionarios.getColumnModel().getColumn(1).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(2).setPreferredWidth(95);
        jTableFuncionarios.getColumnModel().getColumn(2).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(3).setPreferredWidth(95);
        jTableFuncionarios.getColumnModel().getColumn(3).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(4).setPreferredWidth(95);
        jTableFuncionarios.getColumnModel().getColumn(4).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(5).setPreferredWidth(95);
        jTableFuncionarios.getColumnModel().getColumn(5).setResizable(false);
        jTableFuncionarios.getTableHeader().setReorderingAllowed(false);
        jTableFuncionarios.setAutoResizeMode(jTableFuncionarios.AUTO_RESIZE_OFF);
        jTableFuncionarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
   
} 

